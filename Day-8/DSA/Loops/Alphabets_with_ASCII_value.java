package Loops;

import java.util.Scanner;

public class Alphabets_with_ASCII_value {
    public static void main(String[] args) {
        for(int i = 65; i<=90; i++) {
            System.out.println((char)i + " " + i);
        }

        for(int i = 97; i<=122; i++) {
            System.out.println((char)i + " " + i);
        }

    }
}
