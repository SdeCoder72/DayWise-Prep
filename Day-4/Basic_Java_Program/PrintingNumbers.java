package Basic_Java_Program;
public class PrintingNumbers {
    public static void main(String[] args) {
        System.out.print("Numbers : ");
        System.out.println(102 + 43);

        System.out.print("Sting + Number : ");
        System.out.println("102" + 43);

        System.out.print("Number + Number + String: ");
        System.out.println(46 + 24 + "12");

        System.out.println("String : 35 + 23");
    }

}
