package Basic_Java_Program;

public class Arithmetic_Operators {
    public static void main(String[] args) {
        int x = 38;
        int y = 10;
        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
        System.out.println(x % y);
    }
}
