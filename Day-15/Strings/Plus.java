package Strings;

public class Plus {
    public static void main(String[] args) {
        String s = "shanaya";
        s += " is a good girl.";
        System.out.println(s);
        s += 10;
        s += 'a';
        System.out.println(s);
        s += '\n';
        s += 'n';
        System.out.println(s);
        System.out.println("Sabina" + 10 + 20);
        System.out.println("Sabina" + (10+20));
        System.out.println(10 + 20 + "Alishba");
        System.out.println(10+ "Naina" + 20);
        System.out.println((int)('a'));
        System.out.println('A' + 2 + "Abinav");
    }
}
