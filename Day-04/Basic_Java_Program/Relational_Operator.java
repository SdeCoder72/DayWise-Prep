package Basic_Java_Program;

public class Relational_Operator {
    public static void main(String[] args) {
        int x = 10;
        int y = 5;
        System.out.println(x < y);
        System.out.println(x > y);
        System.out.println(x <= y);
        System.out.println(x >= y);
        System.out.println(x == y);
        System.out.println(x != y);
        System.out.println(6+7 == 13);
        System.out.println(6*7 == 13);
        System.out.println(6+7 != 13);
    }
}
